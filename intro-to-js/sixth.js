// (advanced) Mathematical operation shorthand
var tomatoPrice = 6;
var potatoPrice = 4;

// addition shorthand
potatoPrice = potatoPrice + 2;
potatoPrice += 2;
potatoPrice ++;
console.log(potatoPrice);

// subtraction shorthand
tomatoPrice = tomatoPrice - 2;
tomatoPrice -= 2;
tomatoPrice --;
console.log(tomatoPrice);